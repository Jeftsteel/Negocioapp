import Foundation
import UIKit

extension UIColor{
    
    func contrastBlueColor() -> UIColor{
        return UIColor(red: 0.07, green: 0.53, blue: 0.647, alpha: 1)
    }
    
    
    func mainOrangeColor() -> UIColor{
        return UIColor(red: 1.0, green: 0.741, blue: 0.02, alpha: 1)
    }
    
    func accentColdGray() -> UIColor{
        return UIColor(red: 0.18, green: 0.21, blue: 0.247, alpha: 1)
    }
}
